// loading modernizr and jquery, and reply script
function bones_scripts_and_styles() {

  global $wp_styles; // call global $wp_styles variable to add conditional wrapper around ie stylesheet the WordPress way

  if (!is_admin()) {

		// register main stylesheet
		wp_register_style( 'cctg-stylesheet', get_stylesheet_directory_uri() . '/library/css/main.css', array(), '', 'all' );

		// ie-only style sheet
		wp_register_style( 'cctg-ie', get_stylesheet_directory_uri() . '/library/css/ie.css', array(), '' );

    // comment reply script for threaded comments
    if ( is_singular() AND comments_open() AND (get_option('thread_comments') == 1)) {
		  wp_enqueue_script( 'comment-reply' );
    }

		//adding scripts file in the footer
		wp_register_script( 'cctg-js', get_stylesheet_directory_uri() . '/library/js/min/main-ck.js', array( 'jquery' ), '', true );

		//adding Jquery as hosted CDN
		if (!is_admin()) add_action("wp_enqueue_scripts", "my_jquery_enqueue", 11);
			function my_jquery_enqueue() {
			   wp_deregister_script('jquery');
			   wp_register_script('jquery', "http" . ($_SERVER['SERVER_PORT'] == 443 ? "s" : "") . "://ajax.googleapis.com/ajax/libs/jquery/1.11.0/jquery.min.js", false, null);
			   wp_enqueue_script('jquery');
			}
		
		// enqueue styles and scripts
		wp_enqueue_style( 'cctg-stylesheet' );
		wp_enqueue_style( 'cctg-ie-only' );

		$wp_styles->add_data( 'cctg-ie-only', 'conditional', 'lt IE 9' ); // add conditional wrapper around ie stylesheet

		wp_enqueue_script( 'jquery' );
		wp_enqueue_script( 'cctg-js' );

	}
}



